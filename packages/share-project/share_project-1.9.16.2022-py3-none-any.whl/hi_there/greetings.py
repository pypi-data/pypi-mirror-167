# say hi in 10 languages
def say_hi(language):
    """Print a greeting in the given language."""
    if language == "en":
        print("Hello!")
    elif language == "fr":
        print("Bonjour!")
    elif language == "de":
        print("Hallo!")
    elif language == "es":
        print("Hola!")
    elif language == "it":
        print("Ciao!")
    elif language == "pt":
        print("Olá!")
    elif language == "ja":
        print("こんにちは！")
    elif language == "zh":
        print("你好！")
    elif language == "ru":
        print("Привет!")
    elif language == "ar":
        print("مرحبا!")
    else:
        print("Sorry, I don't know that language.")
        
        
        
        